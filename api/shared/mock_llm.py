# -----------------------------------------------------------------------------
# Optional real LLM enhancement (disabled by default)
# -----------------------------------------------------------------------------

def enhance_with_llm(system_prompt: str, user_prompt: str, fallback_text: str) -> str:
    """Attempt to enhance `fallback_text` using a real LLM if enabled.

    Always returns usable text. If ENABLE_REAL_LLM is false, no API key is
    configured, or the call fails for any reason, `fallback_text` (produced
    by the template generators above) is returned unchanged.
    """
    settings = get_settings()
    if not settings.enable_real_llm or not (settings.openai_api_key or settings.llm_base_url):
        return fallback_text

    try:
        from openai import OpenAI  # type: ignore

        client = OpenAI(
            # Ollama's OpenAI-compatible endpoint ignores the key but the
            # client requires a non-empty string.
            api_key=settings.openai_api_key or "ollama",
            base_url=settings.llm_base_url or None,
        )
        response = client.chat.completions.create(
            model=settings.llm_model,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_prompt},
            ],
            max_tokens=300,
            # Local models (Ollama) need more headroom than a hosted API.
            timeout=30 if settings.llm_base_url else 8,
        )
        content = response.choices[0].message.content
        return content.strip() if content else fallback_text
    except Exception as exc:  # pragma: no cover - optional dependency / network
        logger.warning("Real LLM enhancement failed, using template text: %s", type(exc).__name__)
        return fallback_text
